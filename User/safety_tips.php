<h1>General</h1>
<p>
1.  Be aware! Recognize your vulnerability.</br>
2.  Report all suspicious persons, vehicles and activities to the nearest police station.</br>
3.  Keep your doors locked whenever you find yourself alone in a building.</br>
4.  If you see someone being victimized, get involved and notify the nearest police station.</br>
5.  Remember the location of emergency phones in buildings. Also, phones have been placed in various locations .</br>
</p>
<h1>Walking</h1>
<p>
1.  Avoid traveling alone at night.</br>
2.  Confine walking to well-lit, regular traveled walks and pathways. Avoid shortcuts and keep away from shrubbery, bushes, alleyways, or any other areas where an assailant might be lurking.</br>
3.  Avoid the athletic fields and tennis courts after dark.</br>
4.  Do not accept rides from casual acquaintances.</br>
5.  When walking to your vehicle or residence, have your keys ready in hand.</br>
6.  When being dropped off by taxi or private vehicle, ask the driver to wait until you get inside.</br>
7.  If threatened by an approaching vehicle, run in the opposite direction. The vehicle will have to turn around in order to pursue you.</br>
8.  When getting out of a car, take a look around to make sure that you are not being followed.</br>
</p>

<h1>Elevator</h1>
<p>
1.  If, while waiting for an elevator, you find yourself alone with a stranger, let him take the elevator and wait for its return.</br>
2.  If you are on an elevator with someone who makes you feel uneasy, get off at the next floor.</br>
3.  Always stand near the control panel, where you have access to the alarm and floor buttons.</br>
</p>
<h1>Driving</h1>
<p>
1.  Whenever possible, limit traveling to well-lit, well-traveled roads.</br>
2.  Keep your windows closed and doors locked.</br>
3.  When stopped at traffic lights or stop signs, keep your vehicle in gear. If threatened, sound your horn and drive away as soon as possible.</br>
4.  Consider installing an alarm system with a panic switch.</br>
5.  Avoid stopping in poorly lit, out-of-the-way places.</br>
6.  If you think you are being followed, keep out of isolated areas. Look for a place where there are people, then stop and let the vehicle pass you.</br>
7.  If the vehicle continues to follow, drive to the nearest location where you can get assistance, i.e., petrol stations, shopping centers, police or fire station, etc.</br>
8.  When parking at night, choose well-lit areas. Before getting out of your vehicle, check for people loitering.</br>
9.  Always remove your ignition keys. Lock the vehicle whenever it is unattended.</br>
</p>
<h1>Offices</h1>
<p>
1.  Keep all offices and laboratories locked when not in use.</br>
2.  Make sure that all locking devices are in proper working order</br>.
3.  Utilize a key control system. All department keys should be signed out and collected when not in use. Keys should be issued only when absolutely necessary.</br>
4.  Do not label keys with their use. Use a code system instead.</br>
5.  Keep desks, cabinets, etc., locked when not in use.</br>
6.  Before leaving, check to make sure that no one is hiding and that your area is properly secure.</br>
</p>
<h1>Motor Vehicles</h1>
<p>
1.  Keep your vehicle locked and the windows rolled up tightly.</br>
2.  When parking, choose a well-lit, heavily traveled area.</br>
3.  Never leave your vehicle running when unattended.</br>
4.  Packages, luggage, and other valuables should be locked in the trunk.</br>
5.  Consider having your vehicle ignition, doors, and trunk keyed differently.</br>
6.  Consider the installation of anti-theft devices such as alarm systems, hidden ignition or fuel "kill" switches, steering column ignition switch protectors, steering wheel to brake pedal bar caps, mag wheels, spare tires, etc.</br>
</p>

